package day07_03_08_2022;

public class P02_CompareLoops {
    public static void main(String[] args) {

        int num=10;

        for (int i = 0; i < num; i++) {
            System.out.println("Hello from B-26 FOR ");
        }


        System.out.println("===============");


        while(num<10){  // think first then execute to code --- repeated if
            System.out.println("Hello from B-26 WHILE ");
        }




        System.out.println("===============");

        do {
            System.out.println("Hello from B-26 DO - WHILE ");
        }while(num<10); // do it first and think it later






    }
}
